
package com.nebhale.cyclinglibrary.model;

abstract class AbstractIdentifableSupport implements Identifiable {

    private final Long id;

    protected AbstractIdentifableSupport(Long id) {
        this.id = id;
    }

    @Override
    public final Long getId() {
        return this.id;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        AbstractIdentifableSupport other = (AbstractIdentifableSupport) obj;
        if (id == null) {
            if (other.id != null) {
                return false;
            }
        } else if (!id.equals(other.id)) {
            return false;
        }
        return true;
    }

}
